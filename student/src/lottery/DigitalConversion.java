package lottery;

public class DigitalConversion {
	int no;
	String name;
	
	public DigitalConversion() {}

	public DigitalConversion(int no, String name) {
		this.no = no;
		this.name = name;
	}

	@Override
	public String toString() {
		return "DigitalConversion [no=" + no + ", name=" + name + "]\n";
	}
	
	
}	
