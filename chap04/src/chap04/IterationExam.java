package chap04;

public class IterationExam {
	public static void main(String[] args) {
		// 반복문 
		
		// for, while, do~while
		
		// for : 반복횟수를 정확히 알고있을때 사용, 사용빈도가 가장 높음
		
		// while : 조건에 따른 반복, 조건 먼저검사
		
		// do~while :  조건 나중에검사 << 잘 사용되진 않음
		
		for(/*초기식*/;/*조건식*/;/*증감식*/) {
			// 문장(statement)
		}
		
		// 1. 초기식 : 가장 먼저 수행, 단 한번만 수행
		
		// 2. 조건식 : boolean 참, 거짓을 통해 문장 수행 여부를 결정
		// 참일때만 문장을 수행, 거짓일때는 for 문장을 탈출(종료)
		
		// 3. 문장 : 일반적으로 블럭 {}를 사용하고 문장 수행
		
		// 4. 증감식 : 증감연산자 수행 후 조건식 검사로
	}
}
